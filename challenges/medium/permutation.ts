// See issue: https://github.com/microsoft/TypeScript/issues/31751
type isNever<T> = [T] extends [never] ? true : false

// See explanation: https://github.com/type-challenges/type-challenges/issues/614
type Permutation<T, C = T> = isNever<T> extends true
  ? []
  : C extends C
    ? [C, ...Permutation<Exclude<T, C>>]
    : []

